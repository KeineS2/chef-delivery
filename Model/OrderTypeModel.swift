import Foundation

struct OrderTypeModel: Identifiable{
    let id: Int
    let name: String
    let image: String
}
 // A estrutura OrderTypeModel é projetada para representar um tipo de pedido em um aplicativo. Cada instância da estrutura contém um identificador único, um nome e o nome de uma imagem associada ao tipo de pedido. Esses modelos de tipo de pedido podem ser usados em todo o aplicativo para armazenar e manipular informações sobre os diferentes tipos de pedidos disponíveis.
